function validatePIN(pin) {
  // Проверяем длину PIN-кода
  if (pin.length !== 4 && pin.length !== 6) {
    return false;
  }

  // Проверяем, что все символы являются цифрами
  for (let i = 0; i < pin.length; i++) {
    if (pin[i] < "0" || pin[i] > "9") {
      return false;
    }
  }

  return true;
}

